def _make_synthetic_frame() -> FrameData:
    """Create a minimal valid FrameData for rehearsal events.

    Returns:
        A FrameData with 1x1 pixel JPEG data and is_key_frame=True.
    """
    return FrameData(
        jpeg_data=_MINIMAL_JPEG,
        width=1,
        height=1,
        timestamp=time.time(),
        is_key_frame=True,
    )


class SyntheticCapture:
    """Publishes mock demo events to an event bus without real hardware.

    Simulates a complete demo session by publishing DemoStarted,
    KeyFrameDetected, TranscriptReceived, and DemoStopped events
    with synthetic data. Used by RehearsalPipeline to drive the
    full pipeline chain.

    Args:
        event_bus: The shared event bus to publish events on.
    """

    def __init__(self, event_bus: EventBus) -> None:
        self._event_bus = event_bus

    async def run_demo(
        self,
        team_name: str = "RehearsalTeam",
        track: str = "ROGUE::AGENT",
        duration: float = 180.0,
    ) -> None:
        """Run a complete synthetic demo session.

        Publishes the full event sequence that a real demo would produce:
        DemoStarted -> KeyFrameDetected x3 -> TranscriptReceived x3 ->
        DemoStopped.

        Args:
            team_name: Team name for the rehearsal demo.
            track: Challenge track for scoring context.
            duration: Simulated demo duration in seconds.
        """
        logger.info("Synthetic capture starting demo for team: %s", team_name)

        # 1. Demo started
        self._event_bus.publish(DemoStarted(team_name=team_name))
        await asyncio.sleep(0.5)

        # 2. Key frames (3 synthetic frames)
        for i in range(3):
            frame = _make_synthetic_frame()
            self._event_bus.publish(KeyFrameDetected(frame=frame))
            await asyncio.sleep(0.2)

        # 3. Transcript segments (3 realistic segments)
        for segment in _SYNTHETIC_TRANSCRIPTS:
            self._event_bus.publish(TranscriptReceived(segment=segment))
            await asyncio.sleep(0.2)

        # 4. Demo stopped
        self._event_bus.publish(DemoStopped(team_name=team_name, duration=duration))
        logger.info("Synthetic capture finished demo for team: %s", team_name)
